class Transformer(nn.Module):
    """
    Full Encoder-Decoder Transformer for sequence-to-sequence tasks.

    Args:
        src_vocab_size (int)  : Source vocabulary size.
        tgt_vocab_size (int)  : Target vocabulary size.
        d_model        (int)  : Model dimensionality (default 512).
        N              (int)  : Number of encoder/decoder layers (default 6).
        num_heads      (int)  : Number of attention heads (default 8).
        d_ff           (int)  : FFN inner dimensionality (default 2048).
        dropout        (float): Dropout probability (default 0.1).
    """

    def __init__(
        self,
        src_vocab_size: int = 10000,
        tgt_vocab_size: int = 10000,
        d_model:   int   = 512,
        N:         int   = 6,
        num_heads: int   = 8,
        d_ff:      int   = 2048,
        dropout:   float = 0.1,
        checkpoint_path: str = "checkpoint.pt",
    ) -> None:
        super().__init__()
        # Load vocab, tokenizers inside Transformer.init as requested
        from dataset import Multi30kDataset
        self.dataset_helper = Multi30kDataset(split='train')
        self.src_vocab = self.dataset_helper.src_vocab
        self.tgt_vocab = self.dataset_helper.tgt_vocab
        
        src_vocab_size = len(self.src_vocab)
        tgt_vocab_size = len(self.tgt_vocab)

        self.encoder = Encoder(EncoderLayer(d_model, num_heads, d_ff, dropout), N)
        self.decoder = Decoder(DecoderLayer(d_model, num_heads, d_ff, dropout), N)
        
        if globals().get('USE_LEARNED_POS', False):
            position = LearnedPositionalEncoding(d_model, dropout)
        else:
            position = PositionalEncoding(d_model, dropout)
            
        self.src_embed = nn.Sequential(nn.Embedding(src_vocab_size, d_model), copy.deepcopy(position))
        self.tgt_embed = nn.Sequential(nn.Embedding(tgt_vocab_size, d_model), copy.deepcopy(position))
        self.generator = nn.Linear(d_model, tgt_vocab_size)
        
        if checkpoint_path is not None:
            import gdown
            gdown.download(id="15i4StQfc6s6MjvbV_yewr9zRh4-ClruC", output=checkpoint_path, quiet=False)
            checkpoint = torch.load(checkpoint_path, map_location='cpu')
            if 'model_state_dict' in checkpoint:
                self.load_state_dict(checkpoint['model_state_dict'])
            else:
                self.load_state_dict(checkpoint)

    # ── AUTOGRADER HOOKS ── keep these signatures exactly ─────────────

    def encode(
        self,
        src:      torch.Tensor,
        src_mask: torch.Tensor,
    ) -> torch.Tensor:
        """
        Run the full encoder stack.

        Args:
            src      : Token indices, shape [batch, src_len]
            src_mask : shape [batch, 1, 1, src_len]

        Returns:
            memory : Encoder output, shape [batch, src_len, d_model]
        """
        return self.encoder(self.src_embed(src), src_mask)

    def decode(
        self,
        memory:   torch.Tensor,
        src_mask: torch.Tensor,
        tgt:      torch.Tensor,
        tgt_mask: torch.Tensor,
    ) -> torch.Tensor:
        """
        Run the full decoder stack and project to vocabulary logits.

        Args:
            memory   : Encoder output,  shape [batch, src_len, d_model]
            src_mask : shape [batch, 1, 1, src_len]
            tgt      : Token indices,   shape [batch, tgt_len]
            tgt_mask : shape [batch, 1, tgt_len, tgt_len]

        Returns:
            logits : shape [batch, tgt_len, tgt_vocab_size]
        """
        return self.decoder(self.tgt_embed(tgt), memory, src_mask, tgt_mask)

    def forward(
        self,
        src:      torch.Tensor,
        tgt:      torch.Tensor,
        src_mask: torch.Tensor,
        tgt_mask: torch.Tensor,
    ) -> torch.Tensor:
        """
        Full encoder-decoder forward pass.

        Args:
            src      : shape [batch, src_len]
            tgt      : shape [batch, tgt_len]
            src_mask : shape [batch, 1, 1, src_len]
            tgt_mask : shape [batch, 1, tgt_len, tgt_len]

        Returns:
            logits : shape [batch, tgt_len, tgt_vocab_size]
        """
        memory = self.encode(src, src_mask)
        logits = self.generator(self.decode(memory, src_mask, tgt, tgt_mask))
        return logits


    def infer(self, src_sentence: str) -> str:
        """
        Translates a German sentence to English using greedy decoding.
        
        Args:
            src_sentence: The raw German text.
            
            
        Returns:
            The fully translated English string, detokenized and clean.
        """
        device = next(self.parameters()).device
        self.eval()
        
        # Tokenize and numericalize
        tokens = self.dataset_helper.tokenize_de(src_sentence)
        src_indices = [self.dataset_helper.sos_idx] + [self.src_vocab.get(tok, self.dataset_helper.unk_idx) for tok in tokens] + [self.dataset_helper.eos_idx]
        src_tensor = torch.tensor(src_indices, dtype=torch.long).unsqueeze(0).to(device)
        src_mask = make_src_mask(src_tensor, pad_idx=self.dataset_helper.pad_idx).to(device)
        
        with torch.no_grad():
            memory = self.encode(src_tensor, src_mask)
            ys = torch.tensor([[self.dataset_helper.sos_idx]], dtype=torch.long, device=device)
            
            max_len = min(100, len(src_indices) + 20)
            
            for _ in range(max_len - 1):
                tgt_mask = make_tgt_mask(ys, pad_idx=self.dataset_helper.pad_idx).to(device)
                out = self.decode(memory, src_mask, ys, tgt_mask)
                prob = self.generator(out[:, -1])
                _, next_word = torch.max(prob, dim=1)
                next_word = next_word.item()
                
                ys = torch.cat([ys, torch.tensor([[next_word]], dtype=torch.long, device=device)], dim=1)
                if next_word == self.dataset_helper.eos_idx:
                    break
                    
            tgt_indices = ys[0].tolist()
        
        # Detokenize
        idx2word = {idx: word for word, idx in self.tgt_vocab.items()}
        words = [idx2word.get(idx, '<unk>') for idx in tgt_indices if idx not in [self.dataset_helper.sos_idx, self.dataset_helper.eos_idx, self.dataset_helper.pad_idx]]
        return " ".join(words)